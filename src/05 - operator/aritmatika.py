"""
+ = tambah
- = kurang
* = kali
/ = bagi
// = pembagian dibulatkan
% = sisa bagi
** = pangkat
"""

wallet = 200
bank = 100
hutang = 50

# tambah
tambah = wallet + bank
print(tambah)

# kurang
kurang = wallet - hutang
print(kurang)

# kali
gandain = wallet * 3
print(gandain)

# bagi
kasih = wallet / 10
print(kasih)

# pangkat
print(3**3)